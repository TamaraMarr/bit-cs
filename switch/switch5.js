var month ='B';

switch (month) {
    case 'A':
        console.log('Good Job!!');
        break;
    case 'B':
        console.log('Pretty Good Job!');
        break;
    case 'C':
        console.log('Passed.');
        break;
    case 'D':
        console.log('Not so good.');
        break;
    case 'F':
        console.log('Failed!');
        break;
    default:
        console.log('Unknown grade.');
        break;
}